	<h1>Отправка данных в строке запроса</h1>
	
	<?php
		// инициализация массива
		$course = [
			[
				"Основы программирования", 
				["Введение в PHP", "Переменные", "Константы", "Типы данных", "Строки"]
			],		
			[
				"Функции",
				["Встроенные функции", "Пользовательские функции", "Область видимости переменных"]
			],
			[
				"Управляющие конструкции",
				["Условные операторы", "Циклы", "Конструкции"]
			]
		];

		// вывод данных из массива $course согласно переданных параметров
		// Проверяем, что нужные параметры пришли в строке запроса
		if (isset($_GET["user"]) && isset($_GET["topic"]) && isset($_GET["lesson"])) {

			$user   = $_GET["user"];
			// в строке запроса темы и уроки нумеруются с 1, в массиве - с 0
			$topic  = (int)$_GET["topic"] - 1;
			$lesson = (int)$_GET["lesson"] - 1;

			// проверим, что такие тема и урок существуют
			if (isset($course[$topic]) && isset($course[$topic][1][$lesson])) {
				echo "Пользователь <b>" . htmlspecialchars($user) . "</b> запросил материал:<p>";
				echo "Тема: " . $course[$topic][0] . "<br>";
				echo "Урок: " . $course[$topic][1][$lesson];
			} else {
				echo "Запрошенная тема или урок не найдены.";
			}
		} else {
			echo "Не переданы необходимые GET-параметры (user, topic, lesson).";
		}
	?>
	

